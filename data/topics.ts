// 获取随机题目
export const getRandomTopic = (lang: 'zh' | 'en'): string => {
  const topics = lang === 'zh' ? topicsZh : topicsEn;
  const categories = Object.values(topics);
  const allTopics = categories.flat();
  const randomIndex = Math.floor(Math.random() * allTopics.length);
  return allTopics[randomIndex];
};

// 获取特定分类的随机题目
export const getRandomTopicByCategory = (lang: 'zh' | 'en', category: string): string => {
  const topics = lang === 'zh' ? topicsZh : topicsEn;
  const categoryTopics = topics[category as keyof typeof topics];
  if (!categoryTopics) {
    return getRandomTopic(lang);
  }
  const randomIndex = Math.floor(Math.random() * categoryTopics.length);
  return categoryTopics[randomIndex];
};

